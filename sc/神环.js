// 检查并扣除材料
function deleteitem(阶段名, 等级名) {
    var 进阶需要材料 = 进阶数据[强化物品][阶段名][等级名]["材料"];
    var 进阶需要绑定材料 = 进阶数据[强化物品][阶段名][等级名]["绑定材料"];

    // 先检查材料是否足够
    for (var i = 0; i < 进阶需要材料.length; i++) {
        var 背包绑定物品数量 = 0;
        var 需要物品数量 = 进阶需要材料[i][1];
        var 需要物品id = 进阶需要材料[i][0];
        var 绑定物品id = -1;

        if (进阶需要绑定材料[i]) {
            绑定物品id = 进阶需要绑定材料[i];
            背包绑定物品数量 = cm.itemQuantity(绑定物品id);
        }

        var 背包非绑定物品数量 = cm.itemQuantity(需要物品id);

        if (需要物品id > 0) {
            if (背包绑定物品数量 + 背包非绑定物品数量 < 需要物品数量) {
                return false;
            }
        } else if (需要物品id == 0) {
            if (cm.getPlayer().getMeso() < 需要物品数量) {
                return false;
            }
        }
    }

    // 扣除材料
    for (var i = 0; i < 进阶需要材料.length; i++) {
        var 背包绑定物品数量 = 0;
        var 需要物品数量 = 进阶需要材料[i][1];
        var 需要物品id = 进阶需要材料[i][0];
        var 绑定物品id = -1;

        if (进阶需要绑定材料[i]) {
            绑定物品id = 进阶需要绑定材料[i];
            背包绑定物品数量 = cm.itemQuantity(绑定物品id);
        }

        if (需要物品id > 0) {
            if (背包绑定物品数量 >= 需要物品数量) {
                cm.gainItem(绑定物品id, -需要物品数量);
            } else {
                if (背包绑定物品数量 > 0) {
                    cm.gainItem(绑定物品id, -背包绑定物品数量);
                    cm.gainItem(需要物品id, -(需要物品数量 - 背包绑定物品数量));
                } else {
                    cm.gainItem(需要物品id, -需要物品数量);
                }
            }
        } else if (需要物品id == 0) {
            cm.gainMeso(-需要物品数量);
        }
    }
    return true;
}
