function getLevelIndex(等级) {
	var levels = [70, 90, 110, 120, 130, 140, 150, 160, 170, 180, 190, 200, 210, 220, 230, 240];
	for (var i = 0; i < levels.length; i++) {
		if (等级 <= levels[i]) {
			return i;
		}
	}
	return -1;
}


function rate(材料爆率) {
	var 整数部分 = Math.floor(材料爆率);
	var 小数部分 = 材料爆率 - 整数部分;
	var 获得数量 = 整数部分;
	if (小数部分 > 0) {
		if (Math.random() < 小数部分) {
			获得数量 += 1;
		}
	}
	return 获得数量;
}

