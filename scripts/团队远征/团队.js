var 当前排名 = -1;

function start() {
	status = -1;
	action(1, 0, 0);
}
function action(mode, type, selection) {
	var 等级 = cm.getPlayer().getLevel();
	if (mode == -1) {
		cm.dispose();
	} else {
		if (status >= 0 && mode == 0) {
			cm.sendOk("感谢你的光临！");
			cm.dispose();
			return;
		}
		if (mode == 1) {
			status++;
		} else {
			status--;
		}
		if (status == 0) {
			var text = "";
			text += "当前你本次的伤害排名:\r\n";
			text += "#w名次             伤害              奖励\r\n";
			var toSpawn = cm.获取BOSS伤害排名("希拉");
			for (var i = 0; i < toSpawn.length; i++) {
				if (toSpawn[i].getLeft() == cm.getPlayer().getName()) {
					当前排名 = i + 1;
					// 设置奖励数据属性
					var index = getLevelIndex(等级);
					if (index >= 0 && index < 奖励数据.length) {
						em.setProperty("奖励数据", JSON.stringify(奖励数据[index]));
						em.setProperty("当前排名", 当前排名);
					}
				}
				text += "#rNO." + (i + 1) + "#k " + toSpawn[i].getLeft() + " : " + toSpawn[i].getRight() + "\r\n";
			}
			text += "奖励将在BOSS死亡后自动发放\r\n";
			cm.sendOk(text);
			cm.dispose();
		}
	}
}
function getLevelIndex(等级) {
	var levels = [119, 129, 139, 149, 159, 169, 179, 189, 199, 209, 219, 229, 239, 255];
	for (var i = 0; i < levels.length; i++) {
		if (等级 <= levels[i]) {
			return i;
		}
	}
	return -1;
}

